package class3;

public class E11ShortHandOperators {
    public static void main(String[] args) {
        int number=100;
        number=number+1;
        System.out.println(number);
        //number++ -->increase the value of number by 1

        double num=15.5;
        num++;
        System.out.println(num); //16.5
        //1.int num=10;num+=10; -->20
        //2.double num=20;
        //num-=5;-->15
        //3.float num=11F;
        //num/=2;-->5
        //4.int number=13;
        //number%=4;-->1
        //5.int num=0;
        //num++; -->1
        //6.double num=0;
        //num--; ->-1



        float f=11;
        f/=2;
        System.out.println(f);

    }
}
